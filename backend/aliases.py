# 의원 한자 별칭 자동 등록 (정당 모듈 부산물 — scripts/build_members.py 가 생성)
# 수동 2명(柳榮夏·李憲昇)에서 22대 전체로 확장. 파일이 없으면 정적 사전만 사용 (하위 호환).
# NFKC 정규화 형태도 함께 등록 — DB 는 호환용 한자(柳=U+F9C9), API 는 표준(U+67F3).
import json as _json
import unicodedata as _ud
from pathlib import Path as _Path

_HANJA_FILE = _Path(__file__).parent.parent / "data" / "members" / "hanja_aliases.json"
if _HANJA_FILE.exists():
    for _pair in _json.loads(_HANJA_FILE.read_text(encoding="utf-8")):
        _hanja = _pair["hanja"]
        ALIAS_GROUPS.append({_pair["name"], _hanja, _ud.normalize("NFKC", _hanja)})

# 빠른 조회용 역인덱스: 표기 → 그룹.
# 같은 표기가 여러 그룹에 있으면 합집합으로 병합 — 자동 등록된 유영하 그룹(표준 한자)이
# 수동 그룹(호환용 한자 포함)을 덮어써 검색 별칭이 유실되는 것을 막는다.
_INDEX: dict[str, set[str]] = {}
for group in ALIAS_GROUPS:
    merged = set(group)
    for name in group:
        if name in _INDEX:
            merged |= _INDEX[name]
    for name in merged:
        _INDEX[name] = merged


def expand_aliases(term: str) -> set[str]:
    """용어 하나를 별칭 집합으로 확장한다. 별칭이 없으면 자기 자신만."""
    return set(_INDEX.get(term, {term}))
